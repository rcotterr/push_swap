int	ft_count_if(char **tab, int (*f)(char *))
{
	int j;
	int c;

	j = 0;
	c = 0;
	while (tab[j])
	{
		if ((*f)(tab[j]) == 1)
			c = c + 1;
		j++;
	}
	return (c);
}
